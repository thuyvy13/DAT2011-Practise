# 28. Nhập tên màu và kiểm tra xem màu đó có phải là một trong các màu cơ bản như đỏ, xanh lá cây, xanh dương không.
color = input("Nhập tên màu: ").lower()

if color == "đỏ":
    print(f"{color} là một màu cơ bản.")
elif color == "xanh lá cây":
    print(f"{color} là một màu cơ bản.")
elif color == "xanh dương":
    print(f"{color} là một màu cơ bản.")
else:
    print(f"{color} không phải là một màu cơ bản.")
