# 14. Viết một chương trình yêu cầu người dùng nhập chiều cao và chiều dài đáy của một hình bình hành và tính diện tích của hình bình hành đó. Xuất kết quả diện tích ra màn hình.
h = float(input("Nhập vào chiều cao hình hình bình hành: "))
a = float(input("Nhập vào chiều dài đáy hình hình bình hành: "))

S = a * h
print(f"Thể tích hình hình bình hành là: {S}")